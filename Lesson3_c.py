#3D ARRAYS

import numpy as np

a = np.array([[1, 2], [3,4], [5,6], [7,8]])
b = np. array ([[10,20], [30,40], [50,60], [70,80]])

#Add

print("Addition: \n\n", a + b)

# Sub

print("\nSubtraction: \n\n", a - b)

# Mul

print("\nMultiplication: \n\n", a * b)

# Div

print("\nDivision: \n\n", a / b)